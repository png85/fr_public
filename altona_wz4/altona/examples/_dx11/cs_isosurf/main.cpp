// paint a frame
sInt sGetFrameTime();

void MyApp::OnPaint3D()
{ 
  // set rendertarget

  sSetTarget(sTargetPara(sST_CLEARALL,0xff405060));

  // get timing

  Timer.OnFrame(sGetTime());
  static sInt time;
  if(sHasWindowFocus())
  {
    if(sRENDERER==sRENDER_DX11 && (sGetSystemFlags()&sISF_FULLSCREEN))
      time = sGetFrameTime();
    else
      time = Timer.GetTime();
  }

  // set camera

  View.SetTargetCurrent();
  View.SetZoom(2.0f);
  View.Camera.EulerXYZ(time*0.00011f,time*0.00013f,time*0.00015f);
  View.Camera.l = sVector31(sVector30(View.Camera.k*-20));
  View.Prepare();

  // compute shader

  sCBuffer<CSTestPara> cbc;
  sF32 rnd[12];
  sRandom rg;
  for(sInt i=0;i<12;i++)
    rnd[i] = rg.Float(0.001f)+0.0005f;

  sF32 scale = 0.35f;
  sMatrix34 cmat = View.Camera;
  cmat.Transpose3();
  cbc.Modify();
  cbc.Data->tx = -cmat.i*0.5f;
  cbc.Data->ty = cmat.j*0.5f;
  cbc.Data->ofs.Init(-(DIM-1)/2.0f,-(DIM-1)/2.0f,-(DIM-1)/2.0f,scale);
  sF32 s = 8;
  cbc.Data->potpos[0].Init(sFSin(time*rnd[0])*s,sFSin(time*rnd[4])*s,sFSin(time*rnd[ 8])*s,1);
  cbc.Data->potpos[1].Init(sFSin(time*rnd[1])*s,sFSin(time*rnd[5])*s,sFSin(time*rnd[ 9])*s,1);
  cbc.Data->potpos[2].Init(sFSin(time*rnd[2])*s,sFSin(time*rnd[6])*s,sFSin(time*rnd[10])*s,1);
  cbc.Data->potpos[3].Init(sFSin(time*rnd[3])*s,sFSin(time*rnd[7])*s,sFSin(time*rnd[11])*s,1);
  CS->Draw(GDIM,GDIM,GDIM,&cbc);

  CSWC->Draw(1,1,1);

  // set material

  sCBuffer<MaterialFlatPara> cb;
  cb.Data->mvp = View.ModelScreen;
  cb.Data->ldir.Init(-View.ModelView.i.z,-View.ModelView.j.z,-View.ModelView.k.z,0);
  Mtrl->Set(&cb);

  // draw

  sGeometryDrawInfo di;
  di.Indirect = CSIndirect;
  Geo->Draw(di);

  // debug output

  sF32 avg = Timer.GetAverageDelta();
  Painter->SetTarget();
  Painter->Begin();
  Painter->SetPrint(0,0xff000000,2);
  Painter->SetPrint(0,~0,2);
  Painter->PrintF(10,10,L"%5.2ffps %5.3fms",1000.0f/avg,avg);
  Painter->End();
}

