// initialize resources
 
MyApp::MyApp()
{
  // debug output
 
  Painter = new sPainter;

  // geometry 

  static const sU32 desc[] = 
  {
    sVF_POSITION|sVF_F3,
    sVF_NORMAL|sVF_F3,
    sVF_UV0|sVF_F2,
    sVF_END
  };

  Format = sCreateVertexFormat(desc);

  // there is no good reason to create the index buffer in the compute shader
  // but as an example i do it.

  sInt vmax = (TDIM+1)*(TDIM+1)*(TDIM+1)*GDIM*GDIM*GDIM*3;
  sInt imax = DIM*DIM*DIM*15;

  CSVB = new sCSBuffer(sTEX_BUFFER|sTEX_RAW|sTEX_CS_VERTEX|sTEX_CS_WRITE,vmax*Format->GetSize(0)/4);
  CSIB = new sCSBuffer(sTEX_BUFFER|sTEX_RAW|sTEX_CS_INDEX|sTEX_CS_WRITE,imax);
  Geo = new sGeometry(sGF_TRILIST|sGF_INDEX32,Format);
  Geo->SetVB(CSVB);
  Geo->SetIB(CSIB);

  CSCountVertex = new sCSBuffer(sTEX_BUFFER|sTEX_STRUCTURED|sTEX_CS_COUNT|sTEX_CS_WRITE,1,4);
  CSCountIndex = new sCSBuffer(sTEX_BUFFER|sTEX_STRUCTURED|sTEX_CS_COUNT|sTEX_CS_WRITE,1,4);
  CSIndirect = new sCSBuffer(sTEX_BUFFER|sTEX_UINT32|sTEX_CS_WRITE|sTEX_CS_INDIRECT,5,4);

  sU32 mcmap[256*16];
  for(sInt i=0;i<256*16;i++)
  {
    if(triTableBase[0][i]>=0)
      mcmap[i] = indexmap[triTableBase[0][i]];
    else
      mcmap[i] = 0xffffffff;
  }
  MCMap = new sCSBuffer(sTEX_BUFFER|sTEX_UINT32,256*16,0,mcmap);

  // texture

  sImage img(64,64);
  img.Fill(0x00000000);
  img.Checker(0xffff0000,0xff00ff00,8,8);
  img.Glow(0.5f,0.5f,0.25f,0.25f,0xffffffff,1.0f,1.0f);
  Tex = sLoadTexture2D(&img,sTEX_2D|sTEX_ARGB8888);

  // material

  Mtrl = new MaterialFlat;
  Mtrl->Flags = sMTRL_ZON | sMTRL_CULLOFF;
  Mtrl->Texture[0] = Tex;
  Mtrl->TFlags[0] = sMTF_LEVEL2|sMTF_TILE;
  Mtrl->Prepare(sVertexFormatStandard);

  // light

  sClear(Env);
  Env.AmbientColor  = 0x00202020;
  Env.LightColor[0] = 0x00ffffff;
  Env.LightDir[0].Init(0,0,1);
  Env.Fix();

  // compute shader

  CS = new sComputeShader(CSTest());
  CS->SetUAV(0,CSVB);
  CS->SetUAV(1,CSIB);
  CS->SetUAV(2,CSCountVertex,1);
  CS->SetUAV(3,CSCountIndex,1);
  CS->SetTexture(0,MCMap,sMTF_LEVEL0|sMTF_TILE);
  CS->Prepare();

  CSWC = new sComputeShader(WriteCount());
  CSWC->SetUAV(0,CSCountIndex,0);
  CSWC->SetUAV(1,CSIndirect,0);
  CSWC->Prepare();
}

// free resources (memory leaks are evil)

MyApp::~MyApp()
{
  delete CSVB;
  delete CSIndirect;
  delete MCMap;
  delete Painter;
  delete Geo;
  delete Mtrl;
  delete Tex;
  delete CS;
}

