// paint a frame
sInt sGetFrameTime();

void MyApp::OnPaint3D()
{ 
  // set rendertarget

  sSetTarget(sTargetPara(sST_CLEARALL,0xff405060));

  // get timing

  Timer.OnFrame(sGetTime());
  static sInt time;
  if(sHasWindowFocus())
  {
    if(sRENDERER==sRENDER_DX11 && (sGetSystemFlags()&sISF_FULLSCREEN))
      time = sGetFrameTime();
    else
      time = Timer.GetTime();
  }
  Cam.OnFrame(Timer.GetSlices(),sF32(Timer.GetJitter())/Timer.GetTimeSlice());

  // set camera

  View.SetTargetCurrent();
  View.ClipNear = 0.01f;
  View.SetZoom(2.0f);
  View.Camera.EulerXYZ(time*0.00011f,time*0.00013f,time*0.00015f);
  View.Camera.l = sVector31(sVector30(View.Camera.k*-20));
  Cam.MakeViewport(View);
  View.Prepare();


  // build list of nodes

  Scale = 1.0f/16;
  Pos0.Init(-(DIM-1)/2.0f*Scale,-(DIM-1)/2.0f*Scale,-(DIM-1)/2.0f*Scale);
  NodeIndex = 0;
  NodeBuffer->BeginLoad(&Nodes);
  for(sInt z=0;z<GDIM;z++)
    for(sInt y=0;y<GDIM;y++)
      for(sInt x=0;x<GDIM;x++)
        BuildNode(x,y,z,0);
  NodeBuffer->EndLoad();
  sDPrintF(L"nodes: %d\n",NodeIndex);

  if(NodeIndex>0)
  {
    // compute shader

    sCBuffer<CSTestPara> cbc;

    sMatrix34 cmat = View.Camera;
    cmat.Transpose3();
    cbc.Modify();
    cbc.Data->tx = -cmat.i*0.5f;
    cbc.Data->ty = cmat.j*0.5f;
    cbc.Data->ofs.Init(-(DIM-1)/2.0f,-(DIM-1)/2.0f,-(DIM-1)/2.0f,Scale);
    cbc.Data->potpos[0].x = FineScale;
    CS->Draw(NodeIndex,1,1,&cbc);

    CSWC->Draw(1,1,1);

    // set material

    sCBuffer<MaterialFlatPara> cb;
    cb.Data->mvp = View.ModelScreen;
    cb.Data->ldir.Init(-View.ModelView.i.z,-View.ModelView.j.z,-View.ModelView.k.z,0);
    Mtrl->Set(&cb);

    // draw

    sGeometryDrawInfo di;
    di.Indirect = CSIndirect;
    Geo->Draw(di);
  }

  // debug output

  sF32 avg = Timer.GetAverageDelta();
  Painter->SetTarget();
  Painter->Begin();
  Painter->SetPrint(0,0xff000000,2);
  Painter->SetPrint(0,~0,2);
  Painter->PrintF(10,10,L"%5.2ffps %5.3fms",1000.0f/avg,avg);
  Painter->End();
}

