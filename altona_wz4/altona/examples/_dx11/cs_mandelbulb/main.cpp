sF32 MyApp::func(const sVector31 &cc)
{
  sVector31 c(cc);
  c.x *= FineScale;
  c.y *= FineScale;
  c.z *= FineScale;
  float x = c.x;
  float y = c.y;
  float z = c.z;

  // ===

  const int max = 5;
  int i = 0;
  float ab = 0;
  float ib = 0;
  do
  {
    float r2 = x*x+y*y;
    float r4 = r2*r2;
    float r6 = r2*r4;
    float r8 = r4*r4;

    float x2 = x*x;
    float x4 = x2*x2;
    float x6 = x2*x4;
    float x8 = x4*x4;

    float y2 = y*y;
    float y4 = y2*y2;
    float y6 = y2*y4;
    float y8 = y4*y4;

    float z2 = z*z;
    float z4 = z2*z2;
    float z6 = z2*z4;
    float z8 = z4*z4;

    float a = 1 + (z8 - 28*z6*r2 + 70*z4*r4 - 28*z2*r6) / (r8);

    float xx = a * (x8 - 28*x6*y2 + 70*x4*y4 - 28*x2*y6 + y8);
    float yy = (8*a*x*y) * (x6 - 7*x4*y2 + 7*x2*y4 - y6);
    float zz = (8*z*sqrt(r2)) * (z2-r2) * (z4 - 6*z2*r2 + r4);

    x = c.x+xx;
    y = c.y+yy;
    z = c.z+zz;

    sBool run = ab<2;
    
    ab = run ? x*x+y*y+z*z : ab;
    ib = run ? ib+1 : ib;
    i++;
  }
  while(i<max && ab<2);
  return (i+2/ab)-4.5f;
}

void MyApp::BuildNode(sInt x,sInt y,sInt z,sInt d)
{
  sF32 sd = Scale/sF32(1<<d);
  sAABBox box;
  box.Min.Init(x*TDIM*sd+Pos0.x,y*TDIM*sd+Pos0.y,z*TDIM*sd+Pos0.z);
  box.Max.Init(box.Min.x+(TDIM+1)*sd,box.Min.y+(TDIM+1)*sd,box.Min.z+(TDIM+1)*sd);
  sF32 dist=0;
  if(View.VisibleDist(box,dist))
  {
    sVector31 test[9];
    box.MakePoints(test+0);
    test[8] = box.Center();
    sF32 min = 1000;
    sF32 max = -1000;
    sF32 safe = 0.5f;
    for(sInt i=0;i<9;i++)
    {
      sF32 f = func(test[i]);
      min = sMin(min,f);
      max = sMax(max,f);
    }

    if(min-safe<0 && max+safe>0)
    {
      if(dist<16/(1<<d) && d<15)
      {
        BuildNode(x*2+0,y*2+0,z*2+0,d+1);
        BuildNode(x*2+0,y*2+0,z*2+1,d+1);
        BuildNode(x*2+0,y*2+1,z*2+0,d+1);
        BuildNode(x*2+0,y*2+1,z*2+1,d+1);
        BuildNode(x*2+1,y*2+0,z*2+0,d+1);
        BuildNode(x*2+1,y*2+0,z*2+1,d+1);
        BuildNode(x*2+1,y*2+1,z*2+0,d+1);
        BuildNode(x*2+1,y*2+1,z*2+1,d+1);
      }
      else
      {
        Nodes[NodeIndex].x = x;
        Nodes[NodeIndex].y = y;
        Nodes[NodeIndex].z = z;
        Nodes[NodeIndex].w = 1<<d;
        if(NodeIndex<0x10000)
          NodeIndex++;
      }
    }
  }
}

